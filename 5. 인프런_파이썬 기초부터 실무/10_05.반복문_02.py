# for문을 이용하여 팩토리얼(factorial)을 계산하는 프로그램 작성
fact = 1.0
n = int(input("팩토리얼 값을 구할 정수를 입력 : "))
for i in range(1,n+1):
    fact *= i
print(n, "!은", fact,  "입니다.")    
# 팩토리얼 값을 구할 정수를 입력 : 4
# 4 !은 24.0 입니다.

